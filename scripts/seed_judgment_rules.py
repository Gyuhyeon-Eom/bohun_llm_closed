def main():
    import psycopg
    from config.settings import PG_DSN
    with psycopg.connect(PG_DSN) as conn, conn.cursor() as cur:
        cur.execute("TRUNCATE judgment_rule RESTART IDENTITY")
        for sub, pat, axis, cond, kind, docs, basis in RULES:
            cur.execute("INSERT INTO judgment_rule(subcommittee, disease_pattern, axis,"
                        " condition, check_kind, required_docs, basis)"
                        " VALUES (%s,%s,%s,%s,%s,%s,%s)",
                        (sub, pat, axis, cond, kind, docs, basis))
        conn.commit()
        cur.execute("SELECT subcommittee, count(*) FROM judgment_rule GROUP BY 1 ORDER BY 1")
        print("적재:", dict(cur.fetchall()), f"(총 {len(RULES)}건, 모듈 v2 최종 기준)")


if __name__ == "__main__":
    main()
